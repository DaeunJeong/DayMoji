import UIKit

extension UIColor {
    static let purple = UIColor(named: "Purple")!
    static let lightGray = UIColor(named: "LightGray")!
    static let white = UIColor(named: "White")!
    static let darkGray = UIColor(named: "DarkGray")!
    static let black = UIColor(named: "Black")!
}
